Fs = 100e3;          % 采样率100kHz，100e3就是100000，意思是每秒采集100000个点（点越多，信号越精细）
T = 1e-3;            % 脉冲宽度1ms：1e-3就是0.001秒（1毫秒），也就是这个信号持续的时间
f0 = 10e3;           % 中心频率10kHz：10e3就是10000赫兹（10kHz），信号的基础频率
K = 20e6;            % 调频率：20MHz/s20e6就是20000000，意思是信号频率每秒变化20MHz（频率会越来越高或低）

% 生成时间序列（相当于给信号打"时间戳"）
% 0:1/Fs:T 表示从0开始，每次增加1/Fs（也就是1/100000=0.00001秒），直到达到T（0.001秒）
t = 0:1/Fs:T;      % 这样得到的是一系列时间点，比如0、0.00001、0.00002...0.001秒 

% 生成LFM信号（线性调频信号，频率会随时间变化，像救护车声音由低变高）
% cos()里的是信号的"相位"，决定了信号的变化规律：
% 2*pi*f0*t 是基础频率的相位（固定频率部分）
% pi*K*t.^2 是调频部分（让频率随时间变，t.^2表示时间的平方）
lfm_signal = cos(2*pi*f0*t + pi*K*t.^2);  
    % 生成LFM信号

% 画信号的波形图（看信号随时间的变化）
% t*1e3 是把时间单位从秒换成毫秒（1秒=1000毫秒，方便看图）
% 横轴是时间（毫秒），纵轴是信号的幅度（高低起伏）
plot(t*1e3, lfm_signal)
% 可以加上图的标题和坐标轴名称，更清楚（这里原代码没加，补充一下会更好）
title('LFM信号的波形（时间-幅度）')
xlabel('时间（毫秒）')
ylabel('信号幅度')

% 下面开始分析信号的频谱（看信号里包含哪些频率）
N = length(t); % 计算时间点的数量（也就是信号有多少个点）
% 计算频率序列（确定横轴显示哪些频率）
% Fs*(0:N/2-1)/N 是把频率范围转换成实际的赫兹数，只取前一半（因为频谱是对称的）
f = Fs*(0:N/2-1)/N; 
% 用FFT计算频谱（把信号从"时间域"转到"频率域"，看有哪些频率成分）
% abs()取幅度（频率的强弱），/N是归一化（让幅度大小更合理）
spectrum = abs(fft(lfm_signal,N)/N); 
% 画频谱图
% f/1e3 是把频率单位从赫兹换成千赫兹（1kHz=1000Hz，方便看图）
% 横轴是频率（千赫兹），纵轴是频率的强度
plot(f/1e3, spectrum(1:N/2))
% 加上标题和坐标轴名称
title('LFM信号的频谱（频率-强度）')
xlabel('频率（千赫兹）')
ylabel('频率强度')